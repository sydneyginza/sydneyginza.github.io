var siteLanguage = (function() {
  try { return localStorage.getItem('ginza_lang') || 'en'; } catch(e) { return 'en'; }
})();

function t(key) {
  var dict = LANG_DICT[siteLanguage] || LANG_DICT.en;
  return dict[key] !== undefined ? dict[key] : (LANG_DICT.en[key] !== undefined ? LANG_DICT.en[key] : key);
}

function applyLang() {
  /* Static text via data-i18n */
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var k = el.dataset.i18n; if (k) el.textContent = t(k);
  });
  /* Placeholder text via data-i18n-ph */
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var k = el.dataset.i18nPh; if (k) el.placeholder = t(k);
  });
  /* HTML content via data-i18n-html (for elements containing markup like <br>) */
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var k = el.dataset.i18nHtml; if (k) el.innerHTML = t(k);
  });
  document.documentElement.lang = siteLanguage;
  /* Mark active language option */
  document.querySelectorAll('.lang-option').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === siteLanguage);
  });
}

function setLanguage(lang) {
  if (!LANG_DICT[lang]) return;
  siteLanguage = lang;
  try { localStorage.setItem('ginza_lang', lang); } catch(e) {}
  applyLang();
  /* Re-render all JS-built content so it reflects the new language */
  try { if (typeof onFiltersChanged === 'function') onFiltersChanged(); } catch(e) {}
  try { if (typeof renderHome === 'function') renderHome(); } catch(e) {}
}

document.addEventListener('DOMContentLoaded', function() {
  var langBtn = document.getElementById('langBtn');
  var langDropdown = document.getElementById('langDropdown');
  if (!langBtn || !langDropdown) return;
  langBtn.addEventListener('click', function(e) {
    e.stopPropagation();
    var o=langDropdown.classList.toggle('open');
    langBtn.setAttribute('aria-expanded',String(o));
  });
  langDropdown.querySelectorAll('.lang-option').forEach(function(btn) {
    btn.addEventListener('click', function() {
      setLanguage(btn.dataset.lang);
      langDropdown.classList.remove('open');
    });
  });
  document.addEventListener('click', function(e) {
    if (!langDropdown.contains(e.target) && e.target !== langBtn) {
      langDropdown.classList.remove('open');
      langBtn.setAttribute('aria-expanded','false');
    }
  });
});
